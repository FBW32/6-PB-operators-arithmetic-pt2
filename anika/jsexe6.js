// Q1. In one night, a movie theater sells tickets for 6450 dollars.
// Each ticket costs 15 dollars.
// How many tickets were sold?

let sumSales = 6450;
let priceEach = 15;

console.log(sumSales / priceEach); // 430
// 430 tickets where sold.

// Q2. Sylvia's income is 500 dollars per week.
// How much does Sylvia make every year?

console.log(500 * 52); // 26,000
// Sylvia makes 26,000 per year.

// **Percentage**

// Q3. Calculate the percentage of 17/30. Expected output: number%
// (with string concatenation)

let resultQ3 = (17 * 30) / 100 + "%";
console.log(resultQ3); // 5.1%

let resultQ3Try = `${(17 * 30) / 100}%`;
console.log(resultQ3Try); // 5.1%

// **Geometry Formulas**

// Q4. Calculate the perimeter of a square. Assume each side is 4.75cm.

