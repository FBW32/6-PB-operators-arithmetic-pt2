// Q5. Calculate the perimeter of a triangle.
// Assume the length of the sides are 5cm, 6cm, 7cm.

console.log(5 + 6 + 7); // 18
// The perimeter of the triangle is 18cm.

// Q6. Calculate the area of a square. Each side is 5cm.

