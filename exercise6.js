// Basic Arithmetic Problems

/* Q1. In one night, a movie theater sells tickets for 6450 dollars. Each ticket costs 15 dollars. How many tickets were sold?
*/
let ticketsMoney = 6450;
let eachTicket =15;
console.log("Tickets sold in one night : " + (ticketsMoney / eachTicket)); // 430

/* Q2. Sylvia's income is 500 dollars per week. How much does Sylvia makes every year? */
let sylviasIncome = 500;
let weeksInyear = 52;
console.log("Sylvia makes " + "$" + (sylviasIncome * weeksInyear) + " every year"); // Sylvia makes $26000 every year

// Percentage

/* Q3. Calculate the percentage of 17/30. Expected output: number% */
console.log(((17 / 30) * 100) + "%"); // 56.666666666666664% 

// Geometry Formulas

/* Q4. Calculate the perimeter of a square. Assume each side is 4.75cm. */
let side = 4.75;
console.log("Perimeter of a square is " + (4 * side) + "cm"); // 19

/* Q5. Calculate the perimeter of a triangle. Assume the length of the sides are 5cm, 6cm, 7cm. */
let sideA = 5;
let sideB = 6;
let sideC = 7;
console.log("Perimeter of a triangle is " + (sideA + sideB + sideC) + "cm"); // 18

/* Q6. Calculate the area of a square. Each side is 5cm. */
let eachSide = 5;
console.log("Area of a square is " + (eachSide * eachSide) + "cm"); // 25
